package day05_Concatenation;

public class StudentInfo {

    public static void main(String[] args) {

        String studentName = "Vasilica";
        int age = 34;
        char gender = 'F';
        String schoolName = "Cydeo";
        String phoneNumber = "(+44)1022490679";
        // because we don't use the number for calculus, we can just print it
        String phoneNumber2 = "+(44)0747437406";
        boolean isFullTime = true;
        double GPA = 2.5;

        System.out.println("studentName = " + studentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phoneNumber2 = " + phoneNumber2);
        System.out.println("phoneNumber = " + phoneNumber);
        System.out.println("isFullTime = " + isFullTime);
        System.out.println("GPA = " + GPA);





    }


}
