package day4_Variables;

public class GallonsToLiters {

    public static void main(String[] args) {
        //gallon = 3.7 litters

       int gallons = 1000;
       double litters = gallons * 3.7;

        System.out.println("litters = " + litters);
        System.out.println("gallons = " + gallons);

    }


}
