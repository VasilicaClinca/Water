package day03_EscapeSequences;
/*
  Escape Sequences: MUST be given with double quote
  \n: starts a new line
  \t: paragraph space (tab)
  \\: single back slash to print
  \": double quote



    */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("----------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt s nice to see you all! \nWhat class do we have next week?");

        System.out.println("----------------------------");

        System.out.println("\tJava is cool Programming Language");

        System.out.println("------------------------------");

        System.out.println("/ \\"); // in order to print one backword slash we need to write 2 \\

        System.out.println("-------------------------------");

        System.out.println("My favorite show is \"Games Of Trones\""); // in order to print "





    }

}
